const HEADER = `// GENERATED BY MIDO — DO NOT EDIT`;

/**
 * Convert a 6-char hex string (#RRGGBB) to a Dart Color constructor.
 */
function hexToDartColor(hex: string): string {
  const clean = hex.replace("#", "").toUpperCase();
  return `Color(0xFF${clean})`;
}

/**
 * Encode a hex color with a separate opacity into a single 0xAARRGGBB Dart Color.
 */
function shadowToDartColor(color: string, opacity: number): string {
  const clean = color.replace("#", "").toUpperCase();
  const alpha = Math.round(opacity * 255)
    .toString(16)
    .padStart(2, "0")
    .toUpperCase();
  return `Color(0x${alpha}${clean})`;
}

/**
 * Get the Dart lerp expression for a given extension field type.
 */
function lerpExpr(fieldName: string, type: ExtensionFieldType): string {
  switch (type) {
    case "color":
    case "color-static":
      return `Color.lerp(${fieldName}, other.${fieldName}, t)!`;
    case "number":
    case "number-themed":
      return `lerpDouble(${fieldName}, other.${fieldName}, t)!`;
    case "string":
      return `t < 0.5 ? ${fieldName} : other.${fieldName}`;
  }
}

/**
 * Get the Dart type for a given extension field type.
 */
function dartType(type: ExtensionFieldType): string {
  switch (type) {
    case "color":
    case "color-static":
      return "Color";
    case "number":
    case "number-themed":
      return "double";
    case "string":
      return "String";
  }
}

/**
 * Get the light-mode Dart value for an extension field.
 */
function dartLightValue(field: ResolvedExtensionField): string {
  switch (field.type) {
    case "color": {
      const v = field.value as { readonly light: string; readonly dark: string };
      return hexToDartColor(v.light);
    }
    case "color-static":
      return hexToDartColor(field.value as string);
    case "number":
      return `${field.value as number}`;
    case "number-themed": {
      const v = field.value as { readonly light: number; readonly dark: number };
      return `${v.light}`;
    }
    case "string":
      return `'${field.value as string}'`;
  }
}

/**
 * Get the dark-mode Dart value for an extension field.
 */
function dartDarkValue(field: ResolvedExtensionField): string {
  switch (field.type) {
    case "color": {
      const v = field.value as { readonly light: string; readonly dark: string };
      return hexToDartColor(v.dark);
    }
    case "color-static":
      return hexToDartColor(field.value as string);
    case "number":
      return `${field.value as number}`;
    case "number-themed": {
      const v = field.value as { readonly light: number; readonly dark: number };
      return `${v.dark}`;
    }
    case "string":
      return `'${field.value as string}'`;
  }
}

